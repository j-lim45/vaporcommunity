    private void addToCartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartButtonActionPerformed
        Connection conn = null;
        try {
            conn = DriverManager.getConnection("jdbc:sqlite:vapordb.db");
            
            // checks if the game exists in the library of the user (so that it doesn't buy the same game twice)
            PreparedStatement st = conn.prepareStatement("SELECT account_id FROM ownerships WHERE account_id=? AND game_id=?");
            st.setInt(1, staticVar.userThatIsLoggedIn.id);
            st.setInt(2, staticVar.previewedGame.id);
            ResultSet rs = st.executeQuery();
            
            if (!rs.isBeforeFirst()) {
                addGameToCart();
            } else {
                JOptionPane.showMessageDialog(null, "You already own this game.", "Error", JOptionPane.WARNING_MESSAGE);
            }

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_addToCartButtonActionPerformed

    static void addGameToCart() {
        boolean gameAlreadyInCart = false;
        
        // loops through the cart to check if game is already in the cart
        for (Game g : staticVar.userCart) {
            if (g.id==staticVar.previewedGame.id)   gameAlreadyInCart = true;
        }
        
        System.out.println(gameAlreadyInCart);
        if (!gameAlreadyInCart) {
            staticVar.userCart.add(staticVar.previewedGame);
            JOptionPane.showMessageDialog(null, "Game added to cart.", "Success", JOptionPane.PLAIN_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(null, "Game already in cart.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    
