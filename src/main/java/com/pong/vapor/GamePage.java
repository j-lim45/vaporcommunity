    static String displayGenre() {
        Connection conn = null;
        try {
            conn = DriverManager.getConnection("jdbc:sqlite:vapordb.db");
            
            ResultSet rs = conn.createStatement().executeQuery("SELECT genre_name FROM game_genres JOIN genres ON game_genres.genre_id=genreS.genre_id WHERE game_id=" + String.valueOf(staticVar.previewedGame.id));
            
            String genreName = rs.getString("genre_name");
            conn.close();
            return "Genre: " + genreName;
            

        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return "Genre: NULL"; // in case of null value
    }
    
    
